#include "ft_printf.h"

int	ft_putnbr(int n)
{
	int		len;
	char	*base;

	len = 0;
	base = "0123456789";
	if (n == -2147483648)
	{
		ft_putstr("-2147483648");
		return (11);
	}
	if (n < 0)
	{
		len += ft_putchar('-');
		n = -n;
	}
	if (n >= 10)
		len += ft_putnbr(n / 10);
	len += ft_putchar(base[n % 10]);
	return (len);
}
